/**
 * Logical operator OR ( reperesented by || )
 */

// Check if a given day is a weekend or not
let day = "Friday"
if (day === "Saturday" || day === "Sunday"){
    console.log(`${day} is on the weekend`);
} else {
    console.log(`${day} is not on the weekend`);
}

// The OR || statement means only one of the conditions needs to be true for the IF statement to be executed.
// When the AND && is used both statements needs to be true for the IF statment to be executed.

