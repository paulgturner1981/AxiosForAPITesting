"use strict";

/**
 * Adding elements to an array
 * 1. Assignment operator
 * 2. Using .push()
 */

let array = ["apple", "banana", "cherry"]

// 1. Assignment operator
array[3] = "orange";
console.log(array);
console.log(`Total elements: ${array.length}`);

// 2. Using.push()
array.push("pear");
console.log(array); // Total elements: 5

